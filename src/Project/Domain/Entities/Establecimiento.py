class Establecimiento:
    def __init__(self, id, nombre, direccion, ciudad, estado, tipo, horario, precio, imagen, id_admin):
        self.id = id
        self.nombre = nombre
        self.direccion = direccion
        self.ciudad = ciudad
        self.estado = estado
        self.tipo = tipo
        self.horario = horario
        self.precio = precio
        self.imagen = imagen
        self.id_administrador = id_admin
